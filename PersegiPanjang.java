public class PersegiPanjang {
    // Method untuk menghitung luas persegi panjang dengan panjang dan lebar integer
    public int hitungLuas(int panjang, int lebar) {
        return panjang * lebar;
    }

    // Method untuk menghitung luas persegi panjang dengan panjang dan lebar double
    public double hitungLuas(double panjang, double lebar) {
        return panjang * lebar;
    }
}
